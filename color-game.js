var second = 0;
var minute = 0;

function start_timer() {
    intervalop = setInterval(function(){
    	second = second + 1
        document.getElementById('jam').innerHTML = "0" + minute + ":" + "0" + second ;
        if (second > 9){document.getElementById('jam').innerHTML = "0" + minute + ":" + second ;}
		if (second > 59){second = 0 ; minute = minute + 1 ; document.getElementById('jam').innerHTML = "0" + minute + ":" + second ;}
		if (minute > 9){document.getElementById('jam').innerHTML = minute + ":" + second ;}
	}, 1000);
}
